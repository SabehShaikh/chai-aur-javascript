// while loop

let i = 0;
while (i <= 10) {
  console.log(`value of i is ${i}`);
  i++;
}

let myArray = ["flash", "spiderman", "ironman"];
let arr = 0;

while (arr < myArray.length) {
  console.log(`value is ${myArray[arr]}`);
  arr++;
}

// Do while Loop
let score = 11;

do {
  console.log(`score is ${score}`);
  score++;
} while (score <= 10);
